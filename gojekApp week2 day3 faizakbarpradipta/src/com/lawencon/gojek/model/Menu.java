package com.lawencon.gojek.model;

public class Menu {
	private String name;
	private int price;

	public void setName(String name) {
		this.name = name;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public String getName() {
		return name;
	}

	public int getPrice() {
		return price;
	}

	
}
